# What is operators..??

# Operators are special symbol which are used to perform different calculation..

a, b = 100, 50


# Arithmetic Operators

print(a + b)  # Addition
print(a - b)  # substract
print(a * b)  # Multiplication
print(a / b)  # Division
print(a // b)  # Floor Division
print(a % b)  # Reminder
print(5 ** 3)  # To-The-Power
